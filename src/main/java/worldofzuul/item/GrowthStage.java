package worldofzuul.item;

/**
 * The enum Growth stage.
 *
 * Describes the growth stage at which a {@link Plant} is at.
 *
 */
public enum GrowthStage {
    /**
     * Seed growth stage.
     */
    SEED,
    /**
     * Sprout growth stage.
     */
    SPROUT,
    /**
     * Adult growth stage.
     */
    ADULT,
    /**
     * Dead growth stage.
     */
    DEAD,
    /**
     * Ripe growth stage.
     */
    RIPE
}
